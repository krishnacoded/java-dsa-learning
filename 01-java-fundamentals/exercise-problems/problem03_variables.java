// Declare two integer variables and print their **sum and difference**.
public class problem03_variables {
    public static void main(String[] args) {
        int a = 27;
        int b = 35;
        int sum = a + b;
        int difference = Math.abs(a - b);
        System.out.print("The sum of two numbers is " + sum + " and their difference is " + difference + ".");
    }
}