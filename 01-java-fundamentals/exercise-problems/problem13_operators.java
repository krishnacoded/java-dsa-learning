//  Write a program that compares two numbers and prints which one is larger.
public class problem13_operators {
    public static void main(String[] args) {
        int a = 2 * 5, b = 3 * 4;
        if (a > b) {
            System.out.print(a);
        } else {
            System.out.print(b);
        }
    }
}