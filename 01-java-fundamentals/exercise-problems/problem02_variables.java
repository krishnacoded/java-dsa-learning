//  Write a program to store your **name, age, and height** in variables and print them.
public class problem02_variables {
    public static void main(String[] args) {
        String name = "Krishna";
        int age = 19;
        double height = 172.5;
        System.out.println("Hi, my name is " + name + ".");
        System.out.println("I am " + age + " years old.");
        System.out.println("My height is " + height + " cm.");

    }
}