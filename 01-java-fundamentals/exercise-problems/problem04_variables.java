// Write a program that declares three variables: `length`, `width`, and `height`, and calculates the **volume of a cuboid.
public class problem04_variables {
    public static void main(String[] args) {
        double length = 20;
        double width = 12;
        double height = 15.5;

        double volume = length * width * height;
        System.out.print("The volume of the cuboid is " + volume + " cm^3.");
    }

}